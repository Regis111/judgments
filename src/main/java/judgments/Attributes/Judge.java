package judgments.Attributes;

import java.util.List;

public class Judge {

    String name;
    String function;
    List<SpecialRole> specialRoles;

    public String getName() {
        return name;
    }

    public String getFunction() {
        return function;
    }

    public List<SpecialRole> getSpecialRoles() {
        return specialRoles;
    }

}
